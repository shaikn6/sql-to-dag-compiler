class TestDependencyCorrectness:

    def test_diamond_dependency_pattern(self):
        """A → B, A → C, B + C → D  (diamond)."""
        diamond_sql = """
        CREATE TABLE staging.a AS SELECT id FROM raw.source;

        CREATE TABLE staging.b AS SELECT id FROM staging.a;

        CREATE TABLE staging.c AS SELECT id FROM staging.a;

        CREATE TABLE mart.d AS
        SELECT b.id FROM staging.b b JOIN staging.c c ON b.id = c.id;
        """
        result = compile_sql_string(diamond_sql, dag_id="diamond")
        # b and c both depend on a
        assert "create_b.set_upstream(create_a)" in result
        assert "create_c.set_upstream(create_a)" in result
        # d depends on b and c
        assert "create_d.set_upstream(create_b)" in result
        assert "create_d.set_upstream(create_c)" in result

    def test_output_dag_has_correct_chain(self):
        """Verify the exact chain for the sample Oracle SQL."""
        import os
        example_path = os.path.join(
            os.path.dirname(__file__), "..", "examples", "sample_oracle.sql"
        )
        result = compile_sql_file(example_path)
        lines = [ln.strip() for ln in result.splitlines()]
        assert "create_customer_summary.set_upstream(create_customer_txn)" in lines
        assert "insert_high_value_customers.set_upstream(create_customer_summary)" in lines
