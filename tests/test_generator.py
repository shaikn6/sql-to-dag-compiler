class TestCompileSqlFile:

    def test_compiles_example_file(self):
        import os
        example_path = os.path.join(
            os.path.dirname(__file__), "..", "examples", "sample_oracle.sql"
        )
        result = compile_sql_file(example_path)
        assert "PythonOperator(" in result

    def test_dag_id_defaults_to_file_stem(self):
        import os
        example_path = os.path.join(
            os.path.dirname(__file__), "..", "examples", "sample_oracle.sql"
        )
        result = compile_sql_file(example_path)
        assert 'dag_id="sample_oracle"' in result

    def test_custom_dag_id_overrides_stem(self, tmp_path):
        sql_file = tmp_path / "my_proc.sql"
        sql_file.write_text(SAMPLE_SQL, encoding="utf-8")
        result = compile_sql_file(str(sql_file), dag_id="custom_dag_id")
        assert 'dag_id="custom_dag_id"' in result

    def test_three_tasks_for_three_statements(self, tmp_path):
        sql_file = tmp_path / "proc.sql"
        sql_file.write_text(SAMPLE_SQL, encoding="utf-8")
        result = compile_sql_file(str(sql_file))
        assert result.count("PythonOperator(") == 3

    def test_raises_on_missing_file(self):
        with pytest.raises(FileNotFoundError):
            compile_sql_file("/nonexistent/file.sql")


